package com.logiciel.testinglegacycode.otherclasses;

public class Location {

    private String country;
    private String city;

    public Location(String country, String county) {
        this.country = country;
        this.city = county;
    }

    public String getCountry() {
        return country;
    }

    public String getCity() {
        return city;
    }
}
